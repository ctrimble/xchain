package org.xchain.annotations;

import static java.lang.annotation.ElementType.TYPE;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

import java.lang.annotation.Documented;
import java.lang.annotation.Inherited;
import java.lang.annotation.Retention;
import java.lang.annotation.Target;

/**
 * An annotation to indicate that the given class is optional. XChains should
 * not fail to load if a class that is marked as optional has any undefined
 * dependencies. This is to accommodate optional maven dependencies. 
 * 
 * @author Devon Tackett
 *
 */
@Target(TYPE)
@Retention(RUNTIME)
@Inherited
@Documented
public @interface Optional {

}
